#!/bin/bash
msg=$(echo $@ | sed -e "s/$1 //")
file="$1"
echo '#!/bin/bash' > $file
echo -e "\n- $msg\n" >> README.md
nano $file
git add .
git update-index --chmod=+x $file
git commit -m "$msg"
git push

